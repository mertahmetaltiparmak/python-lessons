# veri turu belirtme (casting)
# otomatik belirlemesi yerine olusturdugumuz variable'a ozel bir tur atayabliriz. asagadaki ornek iyi bir sekilde anlatiyor.

x = str(7) # burada 7 karakterini string veri turunde tutuyor, boyle belirtebiliriz string oldugunu. (string = metinsel ifade)
y = int(7) # burada ise 7 karakterini int veri turunde yani tam sayi halinde tutuyor. int oldugunu ise bu sekilde belirtebiliriz.
z = float(7) # burada ise 7 karakterini float veri turunde yani ondalikli olarak tutuyor. float oldugunu ise bu sekilde belirtebiliriz.
# burada tutmak degerken diger dillerde oldugu gibi ramdeki bos bir adrese atamasi anlamina geliyor. ramde belli bir yer kaplar bu deger.

print(x)
print(y)
print(z)